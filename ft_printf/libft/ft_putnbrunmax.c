#include "libft.h"

void	ft_putnbrunmax(uintmax_t a)
{
	if (a >= 10)
	{
		ft_putnbrunmax(a / 10);
		ft_putnbrunmax(a % 10);
	}
	else
		ft_putchar(a + '0');
}
