#include "libft.h"

int	ft_intlen(intmax_t num)
{
	int len;

	if (num == -9223372036854775807 - 1)
		return (19);
	len = 1;
	if (num < 0)
	{
		len++;
		num = num * -1;
	}
	if (num == 0)
		return (1);
	while ((num /= 10))
		len++;
	return (len);
}
