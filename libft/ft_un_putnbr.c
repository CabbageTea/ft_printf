#include "libft.h"

void	ft_un_putnbr(uintmax_t a)
{
	if (a >= 10)
	{
		ft_un_putnbr(a / 10);
		ft_un_putnbr(a % 10);
	}
	else
		ft_putchar(a + '0');
}
