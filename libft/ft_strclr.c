#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t x;

	x = 0;
	if (s)
	{
		i = ft_strlen(s);
		while (x <= i)
		{
			s[x] = '\0';
			x++;
		}
	}
}
